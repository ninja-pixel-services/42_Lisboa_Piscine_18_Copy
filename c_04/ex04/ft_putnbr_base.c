void	ft_putnbr_base(int nbr, char *base)
{
	char	f_nbr;
	long	n;

	n = nb;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (n >= 10)
		ft_putnumber(n / 10);
	f_nbr = (n % 10) + '0';
	write(1, &f_nbr, 1);
}
